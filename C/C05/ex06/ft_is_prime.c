int	ft_is_prime(int nb)
{
	int long	i;

	i = 2;
	while (i++ < nb)
	{
		if (i * i >= nb)
		{
			break ;
		}
		if (nb % i == 0)
		{
			return (0);
		}
	}
	return (1);
}
